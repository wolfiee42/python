def days_of_week(day):
    if day == 1:
        return "It is Monday"
    if day == 2:
        return "It is Tuesday"
    if day == 3:
        return "It is Wednesday"
    if day == 4:
        return "It is Thursday"
    if day == 5:
        return "It is Friday"
    if day == 6:
        return "It is Saturday"
    if day == 7:
        return "It is Sunday"
    
print(days_of_week(1))